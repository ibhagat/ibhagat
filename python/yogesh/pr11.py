# Tuple example

japcars = ('Datsun', 'Toyota', 'Nissan', 'Subaru')
print ('The count of Japanese Cars : ', len(japcars))

allcars = ('Benz','VolksWagon', 'Skoda',japcars)

print ('Now, the count is : ', len(allcars))

print ('List of all the cars : ', allcars)

print ('The List of Japanese car : ', allcars[3])

print ('The Last car in the Japanese list : ', allcars[3][3])


